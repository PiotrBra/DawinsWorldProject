package Model;

public class MoveTuple {
    public Vector2d oldPosition;
    public Vector2d newPosition;

    public Vector2d getOldPosition() {
        return oldPosition;
    }

    public void setOldPosition(Vector2d oldPosition) {
        this.oldPosition = oldPosition;
    }

    public Vector2d getNewPosition() {
        return newPosition;
    }

    public void setNewPosition(Vector2d newPosition) {
        this.newPosition = newPosition;
    }
}
